/**
 * Sync extension dashboard URLs from PUBLIC_URL.
 * Always includes both development + production origins so one packed
 * extension works on localhost and the live site (progress bridge).
 *
 * Development → prefers .env.local
 * Production  → prefers .env.production
 */
import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

function parseMode(argv) {
	const flag = argv.find((a) => a.startsWith("--mode="));
	if (flag) return flag.slice("--mode=".length);
	const idx = argv.indexOf("--mode");
	if (idx !== -1 && argv[idx + 1]) return argv[idx + 1];
	return process.env.NODE_ENV === "production" ? "production" : "development";
}

function readEnvValue(file, key) {
	const full = path.join(root, file);
	if (!fs.existsSync(full)) return null;
	for (const line of fs.readFileSync(full, "utf8").split(/\r?\n/)) {
		const trimmed = line.trim();
		if (!trimmed || trimmed.startsWith("#")) continue;
		const eq = trimmed.indexOf("=");
		if (eq === -1) continue;
		if (trimmed.slice(0, eq).trim() !== key) continue;
		let value = trimmed.slice(eq + 1).trim();
		if (
			(value.startsWith('"') && value.endsWith('"')) ||
			(value.startsWith("'") && value.endsWith("'"))
		) {
			value = value.slice(1, -1);
		}
		if (value) return value.replace(/\/$/, "");
	}
	return null;
}

function loadPublicUrl(mode) {
	const files =
		mode === "production"
			? [".env.production", ".env"]
			: [".env.local", ".env"];

	for (const file of files) {
		const value = readEnvValue(file, "PUBLIC_URL");
		if (value) return { publicUrl: value, source: file };
	}

	const fromEnv = process.env.PUBLIC_URL?.replace(/\/$/, "");
	if (fromEnv) return { publicUrl: fromEnv, source: "process.env" };

	return { publicUrl: "http://localhost:3000", source: "default" };
}

/** Collect every known dashboard origin so content-bridge injects in all envs. */
function collectDashboardOrigins(primaryUrl) {
	const urls = new Set();
	const add = (raw) => {
		if (!raw) return;
		try {
			urls.add(new URL(raw.replace(/\/$/, "")).origin);
		} catch {
			// ignore invalid
		}
	};

	add(primaryUrl);
	add(readEnvValue(".env.local", "PUBLIC_URL"));
	add(readEnvValue(".env.production", "PUBLIC_URL"));
	add(readEnvValue(".env", "PUBLIC_URL"));
	add(process.env.PUBLIC_URL);
	add("http://localhost:3000");

	// wrangler.jsonc vars.PUBLIC_URL
	const wranglerPath = path.join(root, "wrangler.jsonc");
	if (fs.existsSync(wranglerPath)) {
		const raw = fs.readFileSync(wranglerPath, "utf8");
		const match = raw.match(/"PUBLIC_URL"\s*:\s*"([^"]+)"/);
		if (match?.[1]) add(match[1]);
	}

	return [...urls];
}

const mode = parseMode(process.argv.slice(2));
const { publicUrl, source } = loadPublicUrl(mode);
let origin;
try {
	origin = new URL(publicUrl).origin;
} catch {
	console.error(`Invalid PUBLIC_URL: ${publicUrl}`);
	process.exit(1);
}

const origins = collectDashboardOrigins(publicUrl);
const matches = origins.map((o) => `${o}/*`);

const configPath = path.join(root, "extension", "config.js");
fs.writeFileSync(
	configPath,
	`/* Generated by scripts/sync-extension-config.mjs (${mode} ← ${source}) — do not edit */\nconst PUBLIC_URL = ${JSON.stringify(publicUrl)};\n`,
);

const manifestPath = path.join(root, "extension", "manifest.json");
const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));

const tiktokHosts = ["https://www.tiktok.com/*", "https://*.tiktok.com/*"];
const instagramHosts = [
	"https://www.instagram.com/*",
	"https://*.instagram.com/*",
];
manifest.host_permissions = [...tiktokHosts, ...instagramHosts, ...matches];
manifest.externally_connectable = { matches: [...matches] };

const tiktokScript = manifest.content_scripts.find((c) =>
	c.js?.includes("content-tiktok.js"),
);
const instagramScript = manifest.content_scripts.find((c) =>
	c.js?.includes("content-instagram.js"),
);
const bridgeScript = manifest.content_scripts.find((c) =>
	c.js?.includes("content-bridge.js"),
);
if (tiktokScript) tiktokScript.matches = [...tiktokHosts];
if (instagramScript) instagramScript.matches = [...instagramHosts];
if (bridgeScript) bridgeScript.matches = [...matches];

if (!instagramScript) {
	manifest.content_scripts.push({
		matches: [...instagramHosts],
		js: ["content-instagram.js"],
		run_at: "document_idle",
	});
}

fs.writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);

const publicDir = path.join(root, "public");
const zipPath = path.join(publicDir, "extension.zip");
const extensionDir = path.join(root, "extension");
fs.mkdirSync(publicDir, { recursive: true });
if (fs.existsSync(zipPath)) fs.unlinkSync(zipPath);

try {
	if (process.platform === "win32") {
		const psPath = zipPath.replace(/'/g, "''");
		const psSrc = extensionDir.replace(/'/g, "''");
		execFileSync(
			"powershell",
			[
				"-NoProfile",
				"-Command",
				`Compress-Archive -Path '${psSrc}' -DestinationPath '${psPath}' -Force`,
			],
			{ stdio: "pipe" },
		);
	} else {
		execFileSync(
			"tar",
			["-a", "-c", "-f", zipPath, "-C", root, "extension"],
			{ stdio: "pipe" },
		);
	}
	console.log(`Packed ${path.relative(root, zipPath)}`);
} catch (err) {
	console.warn(
		"Could not pack public/extension.zip.",
		err instanceof Error ? err.message : err,
	);
}

console.log(
	`Synced extension PUBLIC_URL [${mode}] → ${publicUrl} (from ${source})`,
);
console.log(`Bridge matches: ${matches.join(", ")}`);
